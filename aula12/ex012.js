var idade = 12
if (idade <= 3){
    console.log('Vocé é praticamente um bebê')
}
else if (idade <= 12) {
    console.log('Você é uma criança')
} 
else if (idade < 18){
    console.log('Você é um adolescente')
}
else if (idade < 60){
    console.log('Você é um adulto')
}
else if (idade >= 60){
    console.log('Você é um idoso')
}